import 'dart:io';

void main() {
  // Declaration of Variables
  int a = 10;
  // a is a variable
  int b = 18;
  print("The numbers are $a and $b");

  // Long value in integers
  BigInt longvalue;
  longvalue = BigInt.parse("2928372498059949785233");

  print("This is long value :  $longvalue");

  // to print fraction value
  double percentage = 87.345;
  print("This Double datatype : $percentage%");
}
